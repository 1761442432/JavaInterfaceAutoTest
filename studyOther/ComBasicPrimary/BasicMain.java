package main.java.ComBasicPrimary;

public class BasicMain {
    public static void main(String[] a){
        // main 在java中属于程序入口，不是关键字
        // String[] 字符串数组
        String main = "ComBasicPrimary.test";
        System.out.println(main);  // 可以运行通过，说明main不是关键字

        System.out.println(a.length); // 默认String[] a 的长度为0
    }
}
